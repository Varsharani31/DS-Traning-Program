# String Methods

s = "Python is a great Programming Language."

print(s.lower())        # Output: python is a great programming language.
print(s.upper())        # Output: PYTHON IS A GREAT PROGRAMMING LANGUAGE.
print(s.swapcase())     # Output: pYTHON IS A GREAT pROGRAMMING lANGUAGE.
print(s.title())        # Output: Python Is A Great Programming Language.
print(s.capitalize())   # Output: Python is a great programming language.